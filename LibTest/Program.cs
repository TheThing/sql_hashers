                0x90, 0x26, 0x93, 0x0c, 0xcc, 0x24, 0xbd, 0x49, 0x74, 0x8e, 0x65, 0xf9, 0xa6, 0xb2, 0xee, 0x68 },
                data))
            {
                Console.WriteLine("Something is wrong.");
            }
        }

        static bool AreEqual(byte[] a, byte[] b)
        {
            return a != null && b != null && a.SequenceEqual(b);
        }
